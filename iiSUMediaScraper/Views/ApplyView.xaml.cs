namespace iiSUMediaScraper.Views;

/// <summary>
/// A user control for displaying the apply configuration interface.
/// Shows games staged for applying and provides controls for executing the apply operation.
/// </summary>
public sealed partial class ApplyView : UserControl, INotifyPropertyChanged
{
    private MainViewModel? _viewModel;

    /// <summary>
    /// Occurs when a property value changes.
    /// </summary>
    public event PropertyChangedEventHandler? PropertyChanged;

    /// <summary>
    /// Initializes a new instance of the ApplyView.
    /// </summary>
    public ApplyView()
    {
        InitializeComponent();
    }

    /// <summary>
    /// Raises the PropertyChanged event for data binding updates.
    /// </summary>
    protected void OnPropertyChanged([CallerMemberName] string propertyName = "")
    {
        PropertyChanged?.Invoke(this, new PropertyChangedEventArgs(propertyName));
    }

    /// <summary>
    /// Gets or sets the view model for this control.
    /// </summary>
    public MainViewModel? ViewModel
    {
        get => _viewModel;
        set
        {
            if (_viewModel != value)
            {
                _viewModel = value;

                OnPropertyChanged(nameof(ViewModel));
            }
        }
    }
}
